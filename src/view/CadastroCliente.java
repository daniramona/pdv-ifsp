    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
       
       String[] data_part = jtDataNasc.getText().split("/");
       
       String data_nasc = data_part[2] + "-" + data_part[1] + "-" + data_part[0];
       
       DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
       Date date = new Date();
       
         
       Cliente cli = new Cliente(0, tipo_cliente.getSelectedItem().toString(), data_nasc, txtObs.getText(), 0);
       Pessoa pes = new Pessoa(0, jtNome.getText(), jtRgIe.getText(), jtCpfCnpj.getText(), jtEmail.getText(), jtTel.getText(), jtCel.getText(),  dateFormat.format(date));
       EnderecoPessoa endereco_pessoa = new EnderecoPessoa(0, jtRua.getText(), Integer.parseInt(jtNro.getText()), jtCep.getText(), jtBairro.getText(), jtCidade.getSelectedItem().toString(), jtUF.getSelectedItem().toString(), 0);
       EnderecoCliente endereco_cliente = new EnderecoCliente(0, jtRuaCliente.getText(), Integer.parseInt(jtNroCliente.getText()), jtCepCliente.getText(), jtBairroCliente.getText(), jtCidadeCliente.getSelectedItem().toString(), jtUFCliente.getSelectedItem().toString(), 0);
       
       ClienteDAO clienteDao = new ClienteDAO();

       // Validação do formulário CLIENTE
       //Se houver campos vazios, exibe mensagem de erro solicitando preenchimento
        if ( (jtNome.getText().trim().isEmpty()) && (jtEmail.getText().trim().isEmpty()) && 
                (jtDataNasc.getText().trim().isEmpty()) && 
                (jtCpfCnpj.getText().trim().isEmpty()) && (jtRgIe.getText().trim().isEmpty()) && 
                (jtTel.getText().trim().isEmpty())&& (jtCel.getText().trim().isEmpty()) && 
                (jtRua.getText().trim().isEmpty()) && (jtNro.getText().trim().isEmpty()) && 
                (jtBairro.getText().trim().isEmpty()) && (jtCep.getText().trim().isEmpty()) && 
                (jtRuaCliente.getText().trim().isEmpty()) && (jtNroCliente.getText().trim().isEmpty()) && 
                (jtBairroCliente.getText().trim().isEmpty()) && (jtCepCliente.getText().trim().isEmpty()) ) {
            JOptionPane.showMessageDialog(null, "É necessário o preenchimento de todos os campos para efetuar o cadastro! Por favor preencha todos os dados!");
        } else {
	clienteDao.inserirCliente(pes, endereco_pessoa, cli, endereco_cliente);
        CadastroCliente.this.dispose();
        }
              
       jfConsultaCliente jfConsulta = new jfConsultaCliente();
       jfConsulta.listarJTable();
       
    }//GEN-LAST:event_jbSalvarActionPerformed

