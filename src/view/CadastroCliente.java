       public void populateUF() throws SQLException{
       ClienteDAO uf = new ClienteDAO();
       ResultSet rs = uf.toComboBox();
       
        while(rs.next()){                   
            jtUFCliente.addItem(new Item(rs.getInt("id"), rs.getString("uf")));
        }
        
        jtUFCliente.addActionListener (new ActionListener () {
        public void actionPerformed(ActionEvent e) {
            int valor = (jtUFCliente.getSelectedIndex() + 1);
            try {
                populateCidade(valor);
            } catch (SQLException ex) {
                Logger.getLogger(CadastroCliente.class.getName()).log(Level.SEVERE, null, ex);
            }
    }
});
    }
       
      public void populateCidade(int idCidade) throws SQLException{
       ClienteDAO cidade = new ClienteDAO();
       ResultSet rs = cidade.toComboBoxCidade(idCidade);
       jtCidadeCliente.removeAllItems();
        while(rs.next()){              
            jtCidadeCliente.addItem(new Item(rs.getInt("id"), rs.getString("nome")));
        }
    } 
       
