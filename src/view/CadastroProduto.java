    private void jtMargemLucroKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtMargemLucroKeyReleased
        // TODO add your handling code here:
        
        NumberFormat n = new NumberFormat();
      
        Float margem_lucro = n.commaToPoint(jtMargemLucro.getText());
        Float valor_custo = n.commaToPoint(jtValorCusto.getText());
        Float divisao = margem_lucro / 100;

        Float valor_unitario = (valor_custo + (valor_custo * divisao));
        
        
        jtValorUN.setText(n.pointToComma(valor_unitario));
       
       
    }//GEN-LAST:event_jtMargemLucroKeyReleased

