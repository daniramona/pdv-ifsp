    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
        NumberFormat n = new NumberFormat();
        Item selected_categoria = (Item) comboCategoria.getSelectedItem();
        Item selected_marca = (Item) comboMarca.getSelectedItem();
        
        String valor_custo = jtValorCusto.getText();
       /* valor_custo = valor_custo.replace(".", "").replace(",", ".");*/
        
        String margem_lucro = jtMargemLucro.getText();
       /*  margem_lucro = margem_lucro.replace(".", "").replace(",", ".");*/
     
        ProdutoDAO produtoDao = new ProdutoDAO();
        
        if(this.op.equals("editar")){
            Produto p = new Produto(
                Integer.parseInt(jtIdProduto.getText()), 
                jtCodEan.getText(), 
                jtDiscriminacao.getText(), 
                n.commaToPoint(valor_custo), 
                n.commaToPoint(margem_lucro),
                selected_marca.getId(),
                selected_marca.getId()
            );
            
            produtoDao.alterarProduto(p);
        }
        /*else{
            
            Produto p = new Produto(
                0, 
                jtCodEan.getText(), 
                jtDiscriminacao.getText(), 
                n.commaToPoint(valor_custo), 
                n.commaToPoint(margem_lucro),
                selected_marca.getId(),
                selected_marca.getId()
            );

            produtoDao.inserirProduto(p);
            CadastroProduto.this.dispose();
        }*/
        
        /* 
        Validação do formulário PRODUTOS:
        Se houver campos vazios, exibe mensagem de erro solicitando preenchimento*/
        
        if ( (jtCodEan.getText().trim().isEmpty()) && (jtDiscriminacao.getText().trim().isEmpty()) &&  
           (jtValorCusto.getText().trim().isEmpty()) && (jtMargemLucro.getText().trim().isEmpty()) ) {
            JOptionPane.showMessageDialog(null, "É necessário o preenchimento de todos os campos para efetuar o cadastro! Por favor preencha todos os dados!");
        } else {
        Produto p = new Produto(
                0, 
                jtCodEan.getText(), 
                jtDiscriminacao.getText(), 
                n.commaToPoint(valor_custo), 
                n.commaToPoint(margem_lucro),
                selected_marca.getId(),
                selected_marca.getId()
            );
	produtoDao.inserirProduto(p);
        CadastroProduto.this.dispose();
        }
        
       


    }//GEN-LAST:event_jbSalvarActionPerformed

