    public void populateMarca() throws SQLException{
       MarcaProdutoDAO marca = new MarcaProdutoDAO();
       ResultSet rs = marca.toComboBox();
       
        while(rs.next()){                   
            comboMarca.addItem(new Item(rs.getInt("id_marca"), rs.getString("descricao")));
        }
    }
    
    
    public void populateCategoria() throws SQLException{
       CategoriaProdutoDAO cat = new CategoriaProdutoDAO();
       ResultSet rs = cat.toComboBox();
       
        while(rs.next()){                   
            comboCategoria.addItem(new Item(rs.getInt("id_categoria"), rs.getString("descricao")));
        }
    }
    
        public void getLastId(){
        ProdutoDAO produto = new ProdutoDAO();
        int rs = produto.getLastId();
        rs = rs + 1;
 
                
        jtIdProduto.setText(Integer.toString(rs));
    
    }
   
