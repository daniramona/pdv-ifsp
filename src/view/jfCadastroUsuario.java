    public void populateUF() throws SQLException{
       UsuarioDAO uf = new UsuarioDAO();
       ResultSet rs = uf.toComboBox();
       
        while(rs.next()){                   
            jtUf.addItem(new Item(rs.getInt("id"), rs.getString("uf")));
        }
        
        
        jtUf.addActionListener ((ActionEvent e) -> {
            int valor = (jtUf.getSelectedIndex() + 1);
            try {
                populateCidade(valor);
            } catch (SQLException ex) {
                Logger.getLogger(jfCadastroUsuario.class.getName()).log(Level.SEVERE, null, ex);
            }
       });
    }
    


    public void populateCidade(int idCidade) throws SQLException{
       UsuarioDAO cidade = new UsuarioDAO();
       ResultSet rs = cidade.toComboBoxCidade(idCidade);
       jtCidade.removeAllItems();
        while(rs.next()){                   
            jtCidade.addItem(new Item(rs.getInt("id"), rs.getString("nome")));
        }
    } 
