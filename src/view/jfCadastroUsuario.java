    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
        Usuario u;        
        try {
            u = new Usuario(0, jtNomeUser.getText(), jtUsuario.getText(), sha1(jpSenha.getText()));
            UsuarioDAO usuario = new UsuarioDAO();
            usuario.inserirUsuario(u);  
        } catch (NoSuchAlgorithmException ex) {
            Logger.getLogger(jfCadastroUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }
        jfCadastroUsuario jfCadUsu = null;
        try {
            jfCadUsu = new jfCadastroUsuario();
        } catch (SQLException ex) {
            Logger.getLogger(jfCadastroUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }
        jfCadUsu.dispose();
        
    }//GEN-LAST:event_jbSalvarActionPerformed

