    public void listarJTable(){
        
            DefaultTableModel modelo = new DefaultTableModel();
            modelo = (DefaultTableModel) jtableFornecedor.getModel();
            modelo.setNumRows(0);
            
            FornecedorDAO fornDAO = new FornecedorDAO();
            
            for(Fornecedor forn:  fornDAO.listarFornecedor()){
                
                modelo.addRow(new Object[]{
                    
                forn.getIdFornecedor(),
                forn.getNomeFantasia(),
                forn.getCpfCnpj(),
                forn.getRgIe(),
                forn.getEmail(),

                
                  
                        
            });
            }   
        
    }
