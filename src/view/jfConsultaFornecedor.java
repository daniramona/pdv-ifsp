    private void jbExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirActionPerformed
        
        Object[] options = { "Sim", "Não" };        

        if(jtableFornecedor.getSelectedRow() == -1){   
            JOptionPane.showMessageDialog(null, "Selecione um registro!");      
        } 
        else {
            int i = JOptionPane.showOptionDialog(null,
                     "Deseja realmente excluir o registro? Você perderá todos os dados!", "AVISO:",
                     JOptionPane.YES_NO_OPTION, JOptionPane.ERROR_MESSAGE, null,
                     options, options[0]);  

            if (i == JOptionPane.YES_OPTION) {
                int indiceLinha = jtableFornecedor.getSelectedRow();
                int idFornecedor = Integer.parseInt(jtableFornecedor.getValueAt(indiceLinha, 0).toString());

                FornecedorDAO fornecedorDao = new FornecedorDAO();
                fornecedorDao.ExcluirFornecedor(idFornecedor);
            } 
        }
    }//GEN-LAST:event_jbExcluirActionPerformed

